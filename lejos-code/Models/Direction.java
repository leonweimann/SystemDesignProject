package Models;

/**
 * The {@code Direction} enum represents the possible directions that can be taken.
 * It includes the following directions:
 * <ul>
 *   <li>{@link #STRAIGHT} - Represents moving straight ahead.</li>
 *   <li>{@link #LEFT} - Represents turning left.</li>
 *   <li>{@link #RIGHT} - Represents turning right.</li>
 *   <li>{@link #BACK} - Represents turning back or making a U-turn.</li>
 * </ul>
 */
public enum Direction {
    STRAIGHT,
    LEFT,
    RIGHT,
    BACK
}
